/**
 * generate-manifest.js
 * Run this whenever you add new images to either folder.
 *
 * Usage (from ghlforms-main folder):
 *   node generate-manifest.js
 *
 * Output: sketches-data.js
 * Each image is stored as base64 inside the file so the browser
 * never needs to fetch or canvas-decode local files.
 */

const fs   = require('fs');
const path = require('path');

const VALID_EXTENSIONS = ['.jpg', '.jpeg', '.png', '.gif', '.webp'];

const FOLDERS = {
  "Exterior Windows": "./Exterior Windows",
  "Patio":            "./Patio"
};

const MIME = {
  '.jpg':  'image/jpeg',
  '.jpeg': 'image/jpeg',
  '.png':  'image/png',
  '.gif':  'image/gif',
  '.webp': 'image/webp'
};

const manifest = {};

for (const [categoryName, folderPath] of Object.entries(FOLDERS)) {
  const absPath = path.resolve(__dirname, folderPath);

  if (!fs.existsSync(absPath)) {
    console.warn(`⚠️  Folder not found, skipping: ${absPath}`);
    manifest[categoryName] = [];
    continue;
  }

  const files = fs.readdirSync(absPath).filter(file => {
    return VALID_EXTENSIONS.includes(path.extname(file).toLowerCase());
  });

  manifest[categoryName] = files.map(file => {
    const ext      = path.extname(file).toLowerCase();
    const mime     = MIME[ext] || 'image/jpeg';
    const filePath = path.join(absPath, file);
    const b64      = fs.readFileSync(filePath).toString('base64');

    console.log(`  ✅ ${categoryName} / ${file}`);

    return {
      name:   path.basename(file, ext),   // display name (no extension)
      file:   file,                        // original filename
      base64: `data:${mime};base64,${b64}` // embedded image data
    };
  });

  console.log(`📁 ${categoryName}: ${manifest[categoryName].length} image(s) encoded\n`);
}

const output = `// Auto-generated by generate-manifest.js — do not edit manually.
// Re-run: node generate-manifest.js
window.SKETCH_MANIFEST = ${JSON.stringify(manifest, null, 2)};
`;

fs.writeFileSync(path.join(__dirname, 'sketches-data.js'), output);
console.log('✅  sketches-data.js generated with embedded images!');
console.log('⚠️  Note: this file may be large if you have many/big images. That is normal.');